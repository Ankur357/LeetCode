class Solution
{
    public ArrayList<String> permutation(String S)
    {
        //Your code here
        ArrayList<String> result = new ArrayList<>();
        permutationHelper(S, "", result);
        Collections.sort(result);
        return result;
    }
    private static void permutationHelper(String s, String answer, ArrayList<String> result) {
        if (s.length() == 0) {
            result.add(answer);
            return;
        }

        for (int i = 0; i < s.length(); i++) {
            char ch = s.charAt(i);
            String left_substr = s.substring(0, i);
            String right_substr = s.substring(i + 1);
            String rest = left_substr + right_substr;
            permutationHelper(rest, answer + ch, result);
        }
    }
	   
}
